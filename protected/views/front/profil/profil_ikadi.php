                            <h3>Sejarah Lahirnya</h3>
                            <p>Dalam dunia global, dakwah dihadapkan kepada  kondisi  yang sangat komplek. Merebaknya berbagai kejahatan di seluruh sektor jika tidak diimbangi dengan dakwah akan berakibat semakin terhimpitnya ruang kebaikan di pentas kehidupan. Sepanjang perjalanan sejarah umat manusia, kejahatan tidak pernah hengkang kalau tidak diimbangi oleh dakwah yang sistematis. Dakwah yang dilakukan berdasarkan bashirah, dan dilakukan secara bersama-sama dengan manhaj yang jelas telah mampu merubah wajah dunia yang gelap menjadi dunia yang terang benderang. Dakwah tanpa perencanaan yang baik dan dilakukan secara individual tidak akan mampu mengangkat keterpurukan umat dari kompleksitas dunia global.</p>
                            <p>Dengan bergulirnya kesadaran baru ditengah-tengah masyarakat Indonesia akan pentingnya Islam dijadikan solusi kehidupan, diperlukan banyak tenaga yang berada dalam satu shaff dengan visi yang sama untuk mensosialisaikan nilai-nilai Islam yang membawa rahmat di tengah-tengah mereka. Hal ini penting dilakukan agar ajaran Islam diterima secara benar oleh masyarakat dan tidak memunculkan image negatif yang bisa menghambat laju perjalanan dakwah.</p>
                            <p>Bertolak dari kesadaran tersebut, dibentuk sebuah organisasi yang akan menjadi wahana para da’i untuk melakukan kerja bersama mengeluarkan umat dari keterpurukan dan menyebarkan Islam yang hanif nan rahmatan lilalamin di segala bidang.</p>
                            <p>Ikatan Da’i Indonesia yang disingkat dengan IKADI. Didirikan di Jakarta pada hari Jum’at,  tanggal 1 Jumadil Ula 1423 Hijriah bertepatan dengan tanggal 12 Juli  2002 Masehi.</p>
                            <p>Sedangkan definisi Da’I disini adalah seorang atau sekelompok orang Islam yang peduli terhadap lingkungan dan ummatnya, terus menerus meningkatkan kualitas Iman dan Taqwa, kemampuan berpikir, menggali, memahami ajaran Islam secara kaffah dan berusaha mengamalkannya serta menyebarkannya dalam bingkai dakwah Islamiyah yang rahmatan lilalamin.</p>
                            <h3>Tujuan</h3>
                            <p>IKADI adalah Organisasi kemasyarakatan Islam yang bertujuan untuk mewadahi aktivitas para da’i dalam mendayagunakan potensinya untuk kemaslahatan umat dan bangsa melalui aktifitas dakwah Islamiyah yang membawa rahmat. </p>
                            <h3>Visi</h3>
                            <p>Menjadi Lembaga Profesi Da'i yang mampu mengoptimalkan potensi para da'i dalam menegakkan nilai-nilai Islam sebagai rahmatan lil 'alamin.</p>
                            <h3>Misi</h3>
                            <ol>
                                <li>Membangun pemahaman Islam berdasarkan al-Quran dan Sunnah sesuai manhaj ulama salafush shaleh bagi segenap umat manusia.</li>
                                <li>Membangun sikap hidup berislam yang rahmatan lil'alamin.</li>
                                <li>Menyebarkan, mengamalkan dan membela nilai-nilai Islam.</li>
                                <li>Meningkatkan ukhuwah Islamiyah antara ummat.</li>
                                <li>Meningkatkan kemampuan dan peran da'i dalam kehidupan berbangsa dan bernegara.</li>
                            </ol>
                            <h3>Sifat dan Ciri Keorganisasian</h3>
                            <ol>
                                <li>IKADI merupakan organisasi kemasyarakatan yang bersifat ke-Islam-an yang diwujudkan dalam bentuk ukhuwah dan silaturahim dalam membina dan mengembangkan ta’aruf (saling mengenal), ta’awun (saling menolong), dan tausiat (saling berwasiat) di jalan kebenaran guna memperkukuh kesatuan dan persatuan bangsa serta mengangkat harkat dan martabat umat manusia.</li>
                                <li>IKADI adalah organisasi berciri keterbukaan dalam penerimaan anggota, menampung aspirasi, partisipasi, prakarsa, dan dinamika anggota. Berciri kemandirian yang dicerminkan dalam sikap organisasi yang memiliki otonomi dalam pemikiran, pengambilan keputusan, penyelenggaraan kegiatan secara amal jama’i terutama bertumpu pada kemampuan pemikiran, upaya, dan sumber daya sendiri sesuai dengan program yang telah ditetapkan.</li>
                                <li>Berciri kekeluargaan yang diimplementasikan pada pengembangan wawasan kebangsaan dan kebersamaan untuk menumbuhkan sikap kekeluargaan da’i serta berpartisipasi dalam pemersatu umat, masyarakat, bangsa, dan negara.</li>
