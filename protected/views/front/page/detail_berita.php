                        <h1>Antisipasi Lahar Dingin Kelud, Warga Pasang Tanggul</h1>
                        <p class="meta"><span class="date">Senin, 24 Februari 2014</span></p>
                        <div class="text-box">
                            <p>REPUBLIKA.CO.ID, KEDIRI -- Mengantisipasi luapan lahar dingin, warga sejumlah dusun di Desa Wonorejo, Kecamatan Puncu, Kabupaten Kediri terus melakukan antisipasi.</p>
                            <p>Mereka mulai memasang tanggul darurat di sekitar lingkungan rumah mereka guna mengantisipasi dampak sekunder erupsi Gunung Kelud tersebut.</p>
                            <p>Tanggul darurat yang dibuat dari beberapa karung material pasir ini akan dapat dijumpai di jalur utama Kecamatan Pare dengan Kecamatan Kepung.</p>
                            <p>Tepatnya di antara wilayah Desa Gadungan hingga Desa Wonorejo. Warga memasang beberapa karung pasir ini dalam beberapa lapis, baik di depan maupun di samping halaman rumah.</p>
                            <p>“Tanggul darurat ini dipasang untuk mengantisipasi <a href="">luapan lahar dingin Kelud</a>,” jelas Murniati (30), warga Dusun Biro, Desa Wonorejo, Senin (24/2).</p>
                            <p>Antisipasi ini, jelasnya, dilakukan tidak hanya oleh warga dusun Biro. Namun juga dusun lainnya, seperti dusun Lestari yang lokasinya dilewati jalur sungai yang berhulu di kawah Kelud.&nbsp;</p>
                            <p>Lahar dingin erupsi Kelud yang turun pernah meluap dan menutup jalur utama penghubung Kecamatan Pare dengan Kecamatan Kepung tersebut.&nbsp;</p>
                            <p>“Saat inipun warga mulai mengantisipasi, karena masih sering turun hujan di wilayah Kabupaten Kediri ini,” tambahnya.</p>
                            <p>Hal ini juga diamini oleh <b>Muzaiyinudin (48)</b>, warga Dusun Lestari. Pada erupsi Gunung Kelud tahun 1990, lahar dingin Gunung Kelud sempat menyapu pemukiman warga Desa Wonorejo.</p>
                            <p>Selain merusakkan rumah warga, terjangan lahar dingin ini juga telah megakibatkan jatuhnya korban jiwa. “Makanya di kawasan hulu, di wilayah Desa Puncu dibangun dam untuk mengurangi dampak lahar dingin ini,” tambahnya.</p>
                            <p>Meski begitu, lanjutnya, warga tetap mewaspadai kemungkinan dampak sekunder erupsi Gunung Kelud ini. Salah satunya dengan memasang tanggul darurat ini.</p>
                            <p>“Apalagi, material vulkanis Kelud tersebut sudah mengalir deras ke aliran sungai Laharpang, di Kecamatan Puncu, Selasa (18/2) sore, beberapa waktu lalu,” tegas Muzaiyinudin.</p>
